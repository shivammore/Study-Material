#Program For Simple Calculations

print("3 + 4 = ",3+4)
print("3 - 4 = ",3-4)
print("3 * 4 = ",3*4)
print("3 / 4 = ",3/4)
print("3 mod 4 = ",3%4)

#With Initializing Values
x=25
y=12
print("\n\n")
print(x," + ",y," = ",x+y)
print(x," - ",y," = ",x-y)
print(x," * ",y," = ",x*y)
print(x," / ",y," = ",x/y)
print(x," mod ",y," = ",x%y)
print("Integer Division of X&Y",x//y) #Return Integered Division
print("Square Of ",x," = ",x*x) #You can write x^2 as x*x or x**2
print("Cube Of ",y," = ",y**3)

