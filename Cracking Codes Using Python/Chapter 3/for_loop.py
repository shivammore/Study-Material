#Looping - For Loop
for i in range(1,10):
	print(i)
print("----------------------------------------------\n")


for i in range(1,10,1):	#Stepped
	print(i)
print("----------------------------------------------\n")


for i in range(1,10,-1):
	print(i)
print("----------------------------------------------\n")


for i in range(1,10,2):
	print(i)
print("----------------------------------------------\n")


for i in range(1,10,1):	#Stepped
	if i%2 ==0:
		print(i)
print("----------------------------------------------\n")



